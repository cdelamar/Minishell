#include "minishell.h"

#define RESET "\033[0m"
#define ORANGE "\033[38;5;208m"

static void	print_line(const char *line)
{
	printf("%s%s%s\n", ORANGE, line, RESET);
}

static const char	**get_snail_ascii(void)
{
	static const char	*snail_ascii[] = {
		"\033[0m        .=%%-   .=%%-",
		"\033[0m        .%%%-   -%%%-",
		"\033[0m        .+%%-   -%%%:",
		"\033[0m        .-=--   --=-.   \033[38;5;208m+*************+",
		"\033[0m         ----   ----\033[38;5;208m*+******************:",
		"\033[0m         ----+*------\033[38;5;208m*********************+",
		"\033[0m         ----+*+-----\033[38;5;208m***********#######*****",
		"\033[0m         :---------\033[38;5;208m***********####****###*****",
		"\033[0m        .-----------\033[38;5;208m*********###*******###*****",
		"\033[0m      .-------=-------\033[38;5;208m******###***##****##*****",
		"\033[0m  .:-------=======-------:.\033[38;5;208m*###***##***##******",
		"\033[0m  :-----------------------:\033[38;5;208m*##***###***#******-",
		"\033[0m       --------------\033[38;5;208m*******###***##**#*******-",
		"\033[0m       --------------\033[38;5;208m********###**********###-.",
		"\033[0m       --------------\033[38;5;208m=********###********###--.",
		"\033[0m       --------------\033[38;5;208m==********#####***####=-:",
		"\033[0m       .--------------\033[38;5;208m+=+********########-:",
		"\033[0m        ---------------\033[38;5;208m===+*********+-----.",
		"\033[0m        .-----------------\033[38;5;208m======-------:",
	};

	return (snail_ascii);
}

void	print_snail(void)
{
	const char	**snail_ascii;
	int			i;

	snail_ascii = get_snail_ascii();
	i = 0;
	while (snail_ascii[i])
	{
		print_line(snail_ascii[i]);
		i++;
	}
}
